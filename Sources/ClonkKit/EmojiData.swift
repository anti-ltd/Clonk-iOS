        "them", "than", "then", "into", "just", "like", "over", "also", "back",
        "after", "other", "their", "there", "these", "would", "could", "about",
        "which", "while", "where", "being", "doing", "going", "new", "old", "off",
        "now", "how", "why", "who", "get", "got", "see", "way", "day", "two", "ten",
    ]
